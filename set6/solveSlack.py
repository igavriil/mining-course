points = [(1, 2, -1), (1, 4, -1), (1, 6, +1), (1, 8, +1), (3, 2, -1), (3, 4, +1), (3, 6, +1), (3, 8, +1), (5, 4, -1), (5, 6, -1), (5, 8, -1), (5, 10, +1), (7, 4, -1), (7, 6, -1), (7, 8, +1), (7, 10, +1)]
 
w1 = 1
w2 = -1
b = -2

for point in points:
    x1, x2, y = point
    part = y * (w1 * x1 + w2 * x2 + b)
    if y > 0:
        e = 1 - part
    else:
        e = 1 + part
    print (x1, x2), e
